emit_ids | xargs -P "$PAR" -I {} sh -c '
  id="$1"
  out="$id.zip"
  echo ">> $out"
  if ! curl -fL -C - --retry 5 --retry-delay 5 --retry-connrefused \
            -o "$out" "'"$BASE"'/$id"; then
    echo "FAILED $id" >> figshare_failed.log
    echo "!! FAILED $id"
  fi
' _ {}

if [ -s figshare_failed.log ]; then
  echo "Some downloads failed; see figshare_failed.log"
  exit 1
fi
echo "All downloads complete."
